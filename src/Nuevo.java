    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        String input = txtRut.getText();
        if(input.matches("\\d+")){
            try{
            int valor = Integer.parseInt(input) * -1;
            stm=conex.createStatement();
            ResultSet lista = stm.executeQuery("SELECT * FROM usuarios WHERE rutUsuario = " + valor);
            if(lista.next()){
                if(BCrypt.checkpw(pswPassword.getText(), lista.getString("password") )){
                    int valor1;
                    int valor2;
                    if(rdoCrear.isSelected()){
                        valor1 = 0;
                    }else{
                        valor1 = 1;
                    }
                    if(rdoSi.isSelected()){
                        valor2 = 0;
                        JOptionPane.showMessageDialog(null, "Nuevo Administrador a Crear.");
                    }
                    else{
                        valor2 = 1; 
                    }
                    this.dispose();
                    new Crear(valor1,valor2).setVisible(true);
                }
                
            }
            else{
                JOptionPane.showMessageDialog(null, "Usuario No Existe");
            }
            }catch(SQLException ex){
                JOptionPane.showMessageDialog(null, "Error en database: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Rut debe Tener solo Numeros");
        }
        
        
    }//GEN-LAST:event_btnConfirmarActionPerformed

