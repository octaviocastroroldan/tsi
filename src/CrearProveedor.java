    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        String input1 = txtNombre.getText();
        String input2 = txtRut.getText();
        String input3 = txtTipo.getText();
        String input4 = txtCorreo.getText();
        String input5 = txtTelefono.getText();
        String direccion = txtDireccion.getText();
        if(input2.matches("\\d+") && input5.matches("\\d+")){
            int valorRut = Integer.parseInt(input2);
            int valorTelefono = Integer.parseInt(input5);
            if(input1.length()==0 || input3.length()==0 || input4.length()==0){
                JOptionPane.showMessageDialog(null, "Conplete Datos", "Advertencia", JOptionPane.WARNING_MESSAGE);
                return;
            }
            try{
                int conteo = 0;
                stm=conex.createStatement();
                ResultSet fila2 = stm.executeQuery("SELECT COUNT(*) FROM proveedores WHERE rutEmpresa = " + valorRut + " OR nomEmpresa = '" + input1 + "'");
                while(fila2.next()){
                    conteo = conteo + fila2.getInt(1);
                }
                if(conteo>0){
                    JOptionPane.showMessageDialog(null, "Proveedor Ya Existe (Revisar Rut y/o Nombre Empresa en Ventana Proveedores", "Advertencia", JOptionPane.WARNING_MESSAGE);
                    return;
                }
                stm.close();
                stm=conex.createStatement();
                stm.executeUpdate("INSERT INTO proveedores VALUES("+valorRut+",'" + input1 + "','" + input3 + "','" + direccion + "','" + input4 + "'," + valorTelefono + ")");
                JOptionPane.showMessageDialog(null, "Proovedor Ingresado");
            }catch(SQLException ex){
                  JOptionPane.showMessageDialog(null, "Error en database: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);               
             }
        }
        else{
            JOptionPane.showMessageDialog(null, "Ingrese al Rut/Telefono SOLO NUMEROS (Rut Digitos Antes del Guion)", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnCrearActionPerformed

